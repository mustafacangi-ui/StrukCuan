-- 1) REWARDS tablosu (ülkeye göre hediye çekleri)
create table if not exists public.rewards (
  id bigint generated by default as identity primary key,
  country_code char(2) not null check (country_code ~ '^[A-Z]{2}$'),
  name text not null,
  description text,
  provider text not null,
  cuan_cost integer not null check (cuan_cost > 0),
  voucher_amount integer not null default 0,
  image_url text,
  created_at timestamptz default now()
);

alter table public.rewards add column if not exists voucher_amount integer not null default 0;

create index if not exists rewards_country_code_idx on public.rewards (country_code);

-- 2) REWARD_CODES tablosu (hazır dijital kodlar)
create table if not exists public.reward_codes (
  id bigint generated by default as identity primary key,
  reward_id bigint not null references public.rewards(id) on delete cascade,
  code text not null,
  user_id text,
  claimed_at timestamptz,
  created_at timestamptz default now(),
  unique (reward_id, code)
);

create index if not exists reward_codes_reward_id_idx on public.reward_codes (reward_id);
create index if not exists reward_codes_available_idx on public.reward_codes (reward_id) where claimed_at is null;

