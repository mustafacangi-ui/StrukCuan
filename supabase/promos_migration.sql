-- Community Promo Sharing System
-- Run in Supabase SQL Editor

-- 0. user_locations table (for 5km push notifications)
create table if not exists public.user_locations (
  user_id uuid primary key references auth.users(id) on delete cascade,
  latitude double precision not null,
  longitude double precision not null,
  updated_at timestamptz default now()
);

create index if not exists user_locations_coords_idx on public.user_locations (latitude, longitude);

alter table public.user_locations enable row level security;

create policy "Users can upsert own location"
  on public.user_locations for all
  using (auth.uid() = user_id)
  with check (auth.uid() = user_id);

-- 1. promos table
create table if not exists public.promos (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  photo_url text not null,
  product_name text not null,
  discount integer not null check (discount >= 1 and discount <= 100),
  store_name text not null,
  latitude double precision not null,
  longitude double precision not null,
  created_at timestamptz default now(),
  status text not null default 'pending' check (status in ('pending', 'verified', 'expired'))
);

create index if not exists promos_user_id_idx on public.promos (user_id);
create index if not exists promos_created_at_idx on public.promos (created_at desc);
create index if not exists promos_status_idx on public.promos (status);
create index if not exists promos_location_idx on public.promos (latitude, longitude);

-- 2. promo_votes table
create table if not exists public.promo_votes (
  id bigint generated by default as identity primary key,
  promo_id bigint not null references public.promos(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  vote_type text not null check (vote_type in ('positive', 'negative')),
  created_at timestamptz default now(),
  unique(promo_id, user_id)
);

create index if not exists promo_votes_promo_id_idx on public.promo_votes (promo_id);
create index if not exists promo_votes_user_id_idx on public.promo_votes (user_id);

